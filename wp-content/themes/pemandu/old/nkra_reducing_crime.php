			<h1>Crime Nkra</h1>

				<p>The overall index crime rate has shown to be increasing over the last few years – from 746 reported crimes per 100,000 population in 2006 to 767 in 2007 and 2008; a rise of nearly 3%. Due to this, it is definitely an issue to be addressed, and we plan to reduce crime through actions focused in delivering three outcomes:</p>
					<ul>
					  <li>Reduced overall reported index crimes, with a focus on street crimes</li>
					  <li>Improved public perception of safety, especially through public participation and volunteerism</li>
					  <li>Improved performance across the criminal justice system to build public confidence and strengthen professional pride across the system</li>
					</ul>
				<p>Currently several street crime reduction initiatives have been deployed, these include:
					<ul>
					  <li>Enhancing police presence in 50 crime hotspots</li>
					  <li>1,100 Pasukan Gerakan Awam (PGA) mobilized in Kuala Lumpur and Selangor</li>
					  <li>Approximately 700 RELA/JPAM personnel were trained and deployed</li>
					  <li>24 units Balai Polis Bergerak assigned to hot spot locations daily</li>
					  <li>23 CCTVs installed in Kuala Lumpur linked to PDRM</li>
					  <li>59 existing CCTVs linked to PDRM</li>
					</ul>
				
<h3>Short-Term Targets </h3>
	<p>2 targets are outlined that are to be achieved soon:</p>
		<ul>
		  <li>5% reduction in overall reported index crime every year for the next three years</li>
		  <li>20% reduction of street crimes by December 2010</li>
		</ul> 	
<h2>Reducing Overall Reported Index Crimes, with a focus on Street Crimes</h2>
	<h3>Reducing overall reported index crimes:</h3>
		<ul>
		  <li><strong>Employing stakeouts </strong>for motorcycle theft, car theft and house      break-ins</li>
		  <li><strong>Improving security features for motorcycles, cars and houses</strong> (including promoting special locks for motorcycles and coding vehicle      parts)</li>
		  <li><strong>Rewarding owners who invest in improved security features </strong>by      encouraging insurers to adjust insurance premiums according to the      security features installed</li>
		  <li><strong>Increasing availability </strong>and <strong>usage of mobile access devices</strong></li>
		  <li>Enabling police offers on      patrol <strong>to check a vehicle’s      ownership status </strong>via a link to Jabatan Pengangkutan Jalan’s (JPJ)      vehicle registry</li>
		</ul>
<h3>Reducing Street Crime:</h3>
	<ul>
	  <li><strong>Making the police omnipresent </strong>in 50 hot spots in four states      (that account for 70% of street crimes)</li>
	  <li><strong>Strengthening law authority presence</strong> with over 3,000 trained      volunteers (including from Jabatan Pertahanan Awam Malaysia (JPAM) and      Ikatan Relawan Rakyat Malaysia (RELA) by June 2010</li>
	  <li>Placing <strong>500 closed-circuit televisions (CCTVs)      in hotspots </strong>to deter criminals</li>
	  <li>Enable <strong>rapid response by the police </strong>and      make it easier to identify offenders</li>
	  <li>Setting up <strong>special courts for quicker prosecution </strong>of street crimes</li>
	  <li>Establishing the <strong>Safe Cities Programme (SCP) </strong>to      bring together relevant agencies to tackle local problems with localized      initiatives, like using bollards to separate pedestrians from motorists</li>
	  <li><strong>Improving in-prison rehabilitation </strong>programmes</li>
	  <li>Introducing <strong>post-release resettlement programmes</strong> to reduce repeat offenders like separating first-time offenders in prisons      from hardcore or repeat convicts.</li>
	</ul>
<h2>Improved Public Perception of Safety, especially through Public Participation and Volunteerism</h2>
		<p>Three parts of the strategy to reduce the fear of becoming a victim of crime as well as improving the public’s perception of safety are:</p>
		<ul>
		  <li>Increasing the public’s sense of safety</li>
		  <li>Encouraging volunteers</li>
		  <li>Communicating with the public on crime</li>
		</ul>
		<p>To measure and monitor the public’s perception of safety, an independent survey will be launched to establish current levels of their safety perception, following that targets will be set out for three year periods to continually ensure the improvement of safety perception.</p>
		<br />
<h3>Increasing the public’s sense of safety:</h3>
		<ul>
		  <li><strong>Assigning senior police officers </strong>(ranked Inspectors and above) <strong>to patrol together </strong>with beat      police for 3-5 hours a week, which will help reassure the public and      motive regular beat policemen.</li>
		  <li>Having <strong>police officers stop and talk to the      general public </strong>for 10-15 minutes of every hour during regular patrols,      which will provide opportunities for the public to raise concerns and get      to know their local officers.</li>
		</ul>
		<h3>Promoting volunteerism:</h3>
		<ul>
  <li><strong>Expanding Skim Rondaan Sukarela (SRS)</strong>, where local residents      patrol their own neighbourhoods</li>
  <li><strong>Expanding Rakan Cop</strong>, an existing scheme where people register      by text messages (SMS) to act as the eyes and hears of the police, where      they can inform the police of criminal incidences or suspicious behavior      via SMS.</li>
</ul>
<h3>Increased communication:</h3>
<ul>
  <li><strong>Communication about</strong> <strong>crime      prevention and crime enforcement </strong>across a variety of <strong>media </strong>will help the public be less      afraid of crime, engage with the police directly and reduce their      susceptibility to criminal acts.</li>
</ul>
<h2>Improved Performance across the Criminal Justice System to build public confidence and strengthen professional pride across the system</h2>
<ul>
  <li><strong>Over 6,000 violent crime cases will be cleared from the backlog</strong>,      which is an increase of almost 1,000 per year.</li>
  <li>To bring <strong>an additional 2,000 violent crime      offenders to trial in 2010 </strong>than went to trial in 2009. </li>
  <li><strong>Improve public satisfaction </strong>with the police force, by      conducting <strong>an independent survey </strong>that      will be commissioned to <strong>measure      current levels of satisfaction</strong> with the police force. </li>
  <li><strong>Establish a baseline</strong> as a target to <strong>improve satisfaction for every year </strong>until 2012. </li>
</ul>
<h3>Reduce the backlog of violent crime cases:</h3>
<ul>
  <li><strong>Increasing the number and efficiency of Investigating Officers      (IOs)</strong> by bringing back retired high-performing IOs for one.</li>
  <li>Creating <strong>Flying Squads or specialist teams</strong> <strong>of high-performing judges and      magistrates </strong>sent to areas with many unsettled cases of violent crime</li>
  <li><strong>Accelerating the bill on Criminal Procedure Code (CPC) </strong>amendments,      including those to implement plea bargaining and tendering of witness      statements, as this would speed up the overall pace of prosecution. </li>
</ul>
<h3>Improving public satisfaction with the police:</h3>
<ul>
  <li><strong>Expediting response time for 999 calls </strong>by instituting caller      tracking and implementing first response via motorbike</li>
  <li><strong>Providing triage and counseling services at police stations</strong></li>
  <li><strong>Publishing league tables of police station performance </strong>against      the overall index crime rate and the change in crime rate</li>
  <li><strong>Highest performing stations will be rewarded, </strong>and the <strong>lowest performers will be supported      and monitored </strong>until their performance improves.</li>
</ul>
