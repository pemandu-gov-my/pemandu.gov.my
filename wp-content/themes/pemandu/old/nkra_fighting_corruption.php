			<h1>NKRA CORRUPTION</h1>

				  <p>Malaysia being a corrupted nation is not what a proud Malaysian would like to hear, and neither does the government. However, sadly this phrase is heard quite rampantly around, and is proven so with Malaysia’s position in the Transparency International (TI) Corruption Perception Index (CPI) dropping from 23 in 1995 to 56 in 2009.
				  <br />
				The cost of corruption to the nation is significant, potentially amounting to as much as RM 10 billion a year, or 1-2% of GDP. Three areas that have been identified as most prone to corruption will be focused on first:</p>
				<ul>
				  <li>Regain public’s confidence in regulatory and enforcement agencies</li>
				  <li>Reduce leakages in government procurement</li>
				  <li>Tackle grand corruption (including political corruption)</li>
				</ul>
				<p>What is targeted is to increase our CPI score from 4.5 to 4.9 in 2010, and an increase from 28% to 37% in TI’s Corruption Barometer of respondents agreeing that the Government’s fight against corruption is effective. </p>
				<br />
				<h3>WHAT YOU CAN EXPECT TO SEE IN 2010</h3>
				<ul>
				  <li>Disclosure of government      procurement contracts on the e-government portal</li>
				  <li>Enactment of the      Whistleblower Protection Law</li>
				  <li>Act to prosecute      corruption cases within one year</li>
				</ul>
				<h3>WHAT HAS BEEN DONE THUS FAR</h3>
				<ul type="disc">
				  <li>Prosecution of elected      representatives (across political parties) and high-ranking civil servants      for misuse of public funds</li>
				  <li>Setting up of a civil      service task force to probe into financial irregularities reported by the      Auditor General</li>
				  <li>A study conducted by the      MACC to plug loopholes for misuse of constituency funds by state      assemblymen and MPs</li>
				  <li>MACC is currently briefing      elected representatives on the procedures that they must put in place to      prevent abuse</li>
				</ul>
				<h3>Regaining the Public’s Confidence in Regulatory and Enforcement Agencies</h3>
				<p>  The public’s confidence needs to be regained in the Police, Customs, Road Transport Department (JPJ) and Immigration, which are the agencies under the Federal Government with the highest number of cases charged by MACC, alongwith the lowest perceived integrity scores by the public.<br />
				The objectives we have laid out are targeted at:</p>
				<ul type="disc">
				  <li>Increasing the number of      cases charged versus number of arrests for drug trafficking and possession      (from 75% to 80%)</li>
				  <li>Increasing the number of      summons settled versus number of summons issued by the PDRM (from 50% to      61%) and JPJ (from 60% to 78%)</li>
				  <li>Increasing the number of      cases charged versus number of arrests and detention under the Immigration      Act (from 53% to 60%)</li>
				  <li>Increasing the taxes      recovered from under-declared goods (from RM9 million to RM21 million)</li>
				  <li>Instituting job rotation      to help prevent enforcement officers from forming collaborative      relationships with criminal organizations</li>
				</ul>
				<p>To achieve these targets, we need to:</p>
				<ul type="disc">
				  <li>strengthen and empower      compliance units within each enforcement agency</li>
				  <li>allocate sufficient      resources to a dedicated unit that is empowered to take action</li>
				</ul>
				<p>These compliance units will report directly to the head of the enforcement agency, with an independent commission overseeing its management and conduct. Other measures including whistleblower protect will also be established to ensure the effectiveness of the compliance units.</p>
				<br/>
				<h3>Reduce leakages in Government Procurement</h3>
				  <p>A reduction of leakages of funds allocated for national development and operational expenditure as well as ensuring transparency in the awarding of contracts is another area of focus.<br />
				  Targetted by 2010 is to reduce the number of audit findings on procurement per Ministry from 11.2 to 10.6. Initiatives to achieve this include:</p>
				<ul type="disc">
				  <li><strong>Gradually reducing the practice of support letters</strong> which are      used to exert undue influence on civil servants to evade the standard      policies and processes in obtaining contracts. Ultimately, we will <strong>eliminate this practice </strong>altogether.</li>
				  <li><strong>Disclosing details of all government procurement contracts </strong>on      the websites of Ministries, agencies and on the e-government portal. This      up-to-date procurement information is the responsibility of the      Secretary-General or Director-General to disclose. Information such as      procurement projects planned, names of successful contractors, contract      sums and project periods are disclosed.</li>
				</ul>
				<h3>TOPIC: Tackling Grand Corruption</h3>
				 <p> The abuse of power and public resources by politicians and senior civil servants needs to be prevented. By 2010, the percentage of trials completed within one year is to be increased from 8.5% to 30%, with the 84 convicted offenders being listed on the new name and shame database.<br />
				  Initiatives to address and rectify this include:</p>
				<ul>
				  <li><strong>Enforce existing political laws </strong>and <strong>conduct a study on revamping political funding </strong>within the bounds of the constitution. This will include the possibility of introducing an <strong>enhanced disclosure process, cap on private donations and supplementary public funding.</strong></li>
				  <li>Announce <strong>zero tolerance policy </strong>and <strong>whistleblower protection framework. </strong></li>
				  <li><strong>Strengthen the</strong> <strong>independence of key institutions </strong>including the Judicial Appointments Commission</li>
				  <li>Endeavour to <strong>complete the prosecution of corruption cases </strong>within one year, particularly public interest cases</li>
				  <li>Enforce <strong>stiffer punishments </strong>for those guilty of corruption.</li>
				  <li>Developing a <strong>public name and shame database </strong>of convicted offenders to act as a deterrent and facilitate employment decisions.</li>
				</ul>
				<p>The success of these measures will be monitored via an independent public survey on the perception of the level of grand corruption in the nation.</p>
