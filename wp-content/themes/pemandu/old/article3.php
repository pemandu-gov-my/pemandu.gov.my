		<h1>Sprucing up bus stops</h1>
		<p>PETALING JAYA: Commuters in the Klang Valley can now benefit from more comfortable waiting areas as 634 bus stops have been refurbished as an initiative under the Government Transformation Programme's (GTP) Urban Public Transport (UPT) National Key Results Area (NKRA). The Ministry of Transport spent between RM5,000 and RM15,000 to refurbish each bus stop.
<br />
<br />
The refurbishment project started in September and was completed in December 2010 and the refurbishment project included tiling, painting, roofing and lighting.
<br />
<br />
These 634 bus stops were the first phase of work which involved seven selected councils consisting of the Shah Alam City Council, Kajang Municipal Council, Klang Municipal Council (MPK), Subang Jaya Municipal Council (MPSJ), Sepang Municipal Council, Selayang Municipal Council and Ampang Jaya Municipal Council (MPAJ).
<br />
<br />
Modern design: One of the bus stop that have been refurbished to provide comfort to commuters in the Klang Valley.
Minister of Transport Datuk Kong Cho Ha said the refurbishment was part of improvements to the public transport network.
<br />
<br />

A total of 306 new locations have also been identified for the construction of new bus stops in the Klang Valley, but this would only start in 2011 as the final costs have yet to be ascertained.

- NST</p>
