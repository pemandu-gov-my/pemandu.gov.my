<h3>Implementing fast, high-impact initiatives immediately and lay the groundwork for longer-term actions</h3>
<p>Initiatives to immediately improve welfare and build income-earning potential will be implemented to ensure sustainable incomes. </p>
<p>To reduce poverty and to eradicate hardcore poverty by the end of 2010, we will immediately:</p>
<ul>
  <li><strong>Distribute federal welfare assistance</strong> (to those that qualify) <strong>on the first day of each month. </strong>This commenced in September 2009 for Peninsular Malaysia and January 2010 for Sabah and Sarawak.</li>
  <li><strong>Provide support to increase home ownership</strong> by offering about 44,000 low-cost houses, for sale and rental, through Dewan Bandaraya Kuala Lumpur (DBKL) and Jabatan Perumahan Negara (JPN).</li>
  <li>Deliver 1 Azam programmes to <strong>create jobs, based on job-matching, training and providing funds.</strong> This will be achieved through a mixture of employment and entrepreneurship (supporting the setup of goods, services and agricultural businesses).</li>
</ul>
<p>By end of 2011, more initiatives will be implemented for these same goals:</p>
<ul>
  <li><strong>Providing training to eligible low-income households</strong> to improve their economic value.</li>
  <li><strong>Creating employment opportunities</strong> via the setting up of Jimat 1 Malaysia provision stores. We are aiming at setting up 50 such stores by 2010 and one in every district by 2011.</li>
  <li>Developing a mechanism to <strong>provide micro-credit to eligible low-income households.</strong> At least one pilot project will be introduced per state by the first quarter of 2011.</li>
  <li><strong>Extending financial support for eligible low-income households to purchase artificial limbs and prosthetics.</strong> We will also improve access to prosthetics by setting up at least one prosthetics centre per state.</li>
</ul>
<p>By end of 2012, some medium-term initiatives will be implemented for these same goals:</p>
<ul>
  <li><strong>Providing additional hostel capacity</strong> to enable students who require accommodation (e.g. those from rural areas) to continue with their studies, preventing dropouts due to distance and financial constraints.</li>
  <li>Intensifying <strong>assistance to patients requiring dialysis treatment.</strong> This will involve providing financial aid, setting up more dialysis centres and providing transportation to and from these centres.</li>
  <li><strong>Offering assistance for down-payments, legal fees and low-interest loans</strong> to eligible low-income households who are not recipients or purchasers of low-cost homes provided by the Government.</li>
  <li><strong>Offering a 1Malaysia insurance scheme</strong>, covering areas such as education, death and critical illness, to hardcore poor households.</li>
</ul>
<p>Over and above all the following initiatives, the feasibility of implementing a minimum wage  is being considered to allow all Malaysians to earn at least the Poverty Line Income (PLI) level.</p>
<p><strong>TOPIC: Putting enablers in place to ensure the delivery of initiatives</strong></p>
<p>To ensure the delivery of the initiatives laid out above, enablers have to be put into place such as:</p>
<ul>
  <li><strong>Creating a unified Social Development Services (SDS) entity</strong> to improve coordination of poverty mitigation initiatives</li>
  <li>Setting up an independent <strong>SDS Inspectorate to investigate and prosecute welfare fraud</strong></li>
  <li><strong>Consolidating all databases</strong> containing data <strong>of LIH</strong> into e-Kasih</li>
  <li><strong>Reducing processing time for disbursement of aid</strong> (from 2 months currently to seven days by 2012)</li>
</ul>
