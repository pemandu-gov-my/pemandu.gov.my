		<h1>NKRA LOW INCOME HOUSEHOLDS</h1>
<p>Despite the successes Malaysia has had in poverty reduction, today there are still over 200,000 households classified as poor and over 40,000 classified as extreme (hardcore) poor.  We are aiming to erase hardcore poverty and reducing the incidence of poverty from 3.6% to 2.8% by the end of 2010. Measures will also be implemented to enhance the productivity of low-income households (LIH) which do not fall under the categories of poor or hardcore poor.</p>
<p>To achieve the above, we are looking at:</p>
<ul>
  <li>Standardising the definition of low-income groups</li>
  <li>Implementing fast, high-impact initiatives immediately and lay the groundwork for longer-term actions</li>
  <li>Putting enablers in place to ensure the delivery of initiatives</li>
</ul>
<h3>What's been achieved so far</h3>
<ul>
  <li>Approval of applications to be listed in the low-income household database now takes seven days, instead of the two months it took previously</li>
  <li>Since 1 September 2009, welfare assistance in Peninsular Malaysia is distributed on the first day of each month</li>
  <li>Approximately 44,000 units of low-cost homes have been offered for sale to existing tenants at prices well below market value (RM21,500 – RM 35,000)</li>
</ul>
