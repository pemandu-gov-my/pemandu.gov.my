		<h1>MACC gears up to fight graft</h1>
		<p>PUTRAJAYA: The Malaysian Anti-Corruption Commission (MACC) and Kumpulan Ikram Sdn Bhd have teamed up to fight graft.

Kumpulan Ikram, through its Ikram Centre for Engineering Forensic & Consultancy Services (ICFC) would assist the Commission in providing the necessary advice in solving corruption cases.
<br /><br />
MACC deputy chief commissioner Datuk Zakaria Jaafar said the cooperation would enable Kumpulan Ikram to assist the Commission in areas such as conducting site investigations, geo-technical advice, building structure inspection, transport and highway planning as well as other engineering-related areas.
<br /><br />
This would enable the MACC Special Operations Division to overcome obstacles faced during high profile case investigations.
<br /><br />
'The Special Operations Division has scrutinised contracts totalling RM2.7bil since it was set up in June.
<br /><br />
'With the assistance of Kumpulan Ikram, we expect a shorter investigation time in future cases,' Zakaria said.
<br /><br />
-Star</p>
