		<h1>Mobile boat clinics for Sabah and Sarawak</h1>
		<p>KUALA LUMPUR: Prime Minister Datuk Seri Najib Razak, the architect of the GTP, recently said the government would introduce the hybrid mobile boat clinic to the people in villages that were connected by rivers in Sabah and Sarawak to provide medical checkups, medicine and advice to those living in areas inaccessible by land.
<br /><br />
'I am determined that in our pursuit of a developed and high-income nation status and in the spirit of inclusiveness, we will not forget the unprivileged,' Najib said.
<br /><br />
'We will go into the deepest forest and up the longest river and over the highest mountain to ensure that all move forward as a nation.
<br /><br />
'It is my dream that all Malaysians will have access to healthcare no matter where they are.
<br /><br />
'A healthy person will be able to contribute more to his family, the economy and the growth of the country,' he said.
<br /><br />
In January 2010, the government introduced the 1Malaysia clinics in urban areas to provide fast and affordable basic treatment to the low-income group as an initiative of the GTP's Low-Income Households (LIH) NKRA.
<br /><br />
As a follow-up to the 1Malaysia Clinics placed in urban areas, the 1Malaysia Mobile Clinic, which would bring free healthcare and check-ups to the doorsteps of remote rural settlements, was launched in December.
<br /><br />
-Star</p>
