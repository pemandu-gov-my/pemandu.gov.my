		<h1>Subsidy Rationalisation</h1>
		<p>To help Malaysia maintain the strong growth it has achieved, the Government has taken the second step in a gradual subsidy rationalisation effort. This step is in line with its long term strategy to provide the resources needed to support the nation's  goal to achieve an inclusive, sustainable and high-income economy.
<br /><br />
The Government has on 4th December announced the subsidy reduction for fuel, specifically petrol, diesel, and liquefied petroleum gas (LPG) as well as sugar. This subsidy rationalisation is necessary to manage the nation's fiscal position by enhancing revenue and at the same time, reducing expenditure. The savings would be channeled towards the Government Transformation Programme (GTP) including improving the urban transportation network, rural basic infrastructure and roads, education and efforts to combat crime.</p>
