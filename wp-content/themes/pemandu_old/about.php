		<h1>GOVERNMENT TRANSFORMATION PROGRAMME </h1>
		<h3>Transforming Malaysia</h3>
		<p>Malaysia has often taken the road less travelled and pragmatically charted its own course. Now, the nation is embarking on the same route once again with bold transformation programmes that no other government in the world has attempted to undertake on that scale.
<br /><br />
The Government Transformation Programme (GTP) was devised in accordance with the principles of 1Malaysia, People First, Performance Now. In its entirety, the GTP is designed to provide all Malaysians access to improved public services irrespective of race, religion and region. 
<br /><br />
The objective of GTP is two-fold - first, to transform the government to be more effective in its delivery of services and accountable for outcomes that matter most to the Rakyat; and second, to move Malaysia forward to become an advanced, united, and just society with high standards of living for all. This is in line with the national mission of achieving Vision 2020 - for Malaysia to become a fully developed nation. 
<br /><br />
Under the GTP, six key priority areas have been identified where challenges within each area have been divided into short-term priorities and long-term issues. These areas of development are known as the National Key Results Areas (NKRAs) as below:
<br /><br />
1)	Reducing Crime 	
2)	Fighting Corruption
3)	Improving Student Outcomes	
4)	Raising Living Standards of Low-Income Households
5)	Improving Rural Basic Infrastructure	
6)	Improving Urban Public Transport
<br /><br />
We realise that we cannot use the same approach as before to transform our government and achieve substantial results quickly. Therefore, to ensure we had an accurate gauge and sense of what the priority areas are for the rakyat, multiple input sources including the rakyat, opinion polls, perception surveys, (alternative and mainstream) media analysis, dialogues with select academics, private and public sector leaders were considered.
<br /><br />
Inevitably, it is no walk in the park and along the way there will be setbacks, missed targets, misjudged initiatives, resilient stakeholders and those unwilling to adhere or adapt to the necessary changes. However, it is vital that all keep the end goal of Vision 2020 and a better life as a developed nation in mind and face the upcoming challenges before reaping the benefits.
 <br /><br />
With the GTP Roadmap, we are ensuring transparency by outlining our objectives, concrete targets and initiatives. We expect to be held accountable for delivering targets we announce and we will publish a progress report on an annual basis, starting in the first quarter of 2011, so that our achievements can be evaluated over time.
</p>
