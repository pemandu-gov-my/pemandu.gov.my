		<h1>NKRA RURAL BASIC INFRASTRUCTURE</h1>
<p>There have been considerable achievements in Malaysia's provision of rural basic infrastructure (RBI), however more still needs to be done to serve the rural population. This population constitutes 35% of total Malaysians. </p>
<p>In Sabah and Sarawak, there are many villages that do not have road connections, access to electricity and even access to clean or treated water. </p>
<p>With these issues in hand, we aspire to:</p>
<ul>
  <li><strong>Build more than 7,000km of new and upgraded roads </strong>by 2012, where 1,900km of these will be in Sabah and Sarawak. With this, it will mean that in Peninsular Malaysia 91.4% of the population will be living within a five kilometer radius to a paved road by 2010, which will be close to 100% by 2012.</li>
  <li><strong>Provide 50,000 new and restored houses to the rural and hardcore poor</strong> by 2012, with two-thirds of these being in Sabah and Sarawak. From this 50,000, over 16,000 will be provided in 2010.</li>
  <li><strong>Ensure access to clean or treated water to over 360,000 additional households.</strong> This will mean in Sabah and Sarawak, the percentage of rural houses with access to clean or treated water will reach approximately 60% in 2010 and 90% in 2012 (currently it stands at 57%).</li>
  <li><strong>Provide access to 24-hour electricity to over 140,000 additional households</strong> by 2012. This will increase the percentage of households with access to electricity in Peninsular Malaysia to 99.6%, Sabah to 81% and Sarawak to 73% in 2010. (Currently they are at 99%, 77% and 67% respectively)</li>
</ul>
<p>To meet said targets, the rate of deployment has to be accelerated. What we need to achieve over the next three years will be <strong>to build 11 times as many kilometers of roads, 2.5 times as many houses, and connect seven times as many houses to clean water, </strong>as compared to 2006-2008. </p>
<p>We aim to reach this rate of deployment by:</p>
<ul>
  <li><strong>Using innovative, quick and cost-effective ways of delivery.</strong> For example, building standard-design houses and using power generation technologies like solar hybrid generation.</li>
  <li><strong>Revamping existing administrative processes to reduce time required.</strong> For example, fine-tuning the road open tender process using standard templates, accelerating communications and more.</li>
  <li>Facilitating and working closely with suppliers to <strong>ensure the supply and availability of enough machinery, material and manpower</strong> when required.</li>
  <li><strong>Ensuring effective programme management by closely monitoring projects.</strong> SPP II, a well-established system managed by the ICU under the Prime Ministerâ€™s Department will be used for recording and tracking RBI initiatives to ensure they are delivered on time and within budgets.</li>
</ul>
<p>In lieu of all of these plans, we have already built an additional 17,000 houses for the rural and hardcore poor and an additional 240km of roads in Sabah and Sarawak in 2009 itself.</p>
