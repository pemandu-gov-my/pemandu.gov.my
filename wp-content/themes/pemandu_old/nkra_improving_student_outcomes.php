			<h1>NKRA EDUCATION</h1>
<p>Malaysia’s education system has progressed by leaps and bounds over the past 50 years with achievements such as:</p>
<ul>
  <li>An adult literacy rate of 92%</li>
  <li>Universal primary school enrolment</li>
  <li>One of the highest growth rates in secondary school enrolment amongst developing countries</li>
</ul>
<p>However, despite these points of pride Malaysia’s education outcomes still fall behind countries in our vicinity such as Singapore, Hong Kong and Korea. At the same time, the gap within Malaysia student outcomes has been widening in recent years, with about 20% failing to meet the minimum TIMSS benchmarks for Math and Science in 2007, as opposed to 5%-7% in 2003.<br />
  An improvement in educational outcomes is critical to building a strong and competitive workforce. Areas of focus that have been identified are:</p>
<ul>
  <li>Increasing the pre-school enrolment rate of both 4+ and 5+ year olds and improving the quality of the system</li>
  <li>Ensuring that all children have acquired basic literacy and numeracy skills after three years of education</li>
  <li>Developing High Performance Schools (HPS) and lift the performance of all schools in the system</li>
  <li>Significantly improving the performance of head teachers and principals via performance management</li>
</ul>
<h3>Programmes implemented in 2010</h3>
<p>  Some of the initiatives have begun execution such as:</p>
<ul>
  <li>Completion of the screening instrument </li>
  <li>Year 1 teaching and learning modules for the Literacy and Numeracy Programme (LINUS)</li>
  <li>Announcement of the first batch of the 20 High Performing Schools (HPS)</li>
  <li>Completion of the performance ranking of the 9.900 government schools (which is a basis to design rewards and remedial programmes for head teachers focus area)</li>
  <li>Trust schools initiative underway with first trust schools expected to operate by January 2011</li>
</ul>
<h3>Increasing the pre-school enrolment rate of both 4+ and 5+ year olds and improving the quality of the system</h3>
 <p>A target enrolment rate of 72% by 2010 end and 87% by 2012 end has been set, from the current rate of 67%. Seven ways have been identified to assist in achieving this:</p>
<ul>
  <li><strong>Establishing the National Committee on Pre-School Education </strong>and a <strong>Pre-School Division within the Ministry of Education </strong>to govern all pre-school providers</li>
  <li>Rolling out the <strong>National Pre-School Curriculum Standard (NPCS), </strong>developed in collaboration with the private sector in January 2010.</li>
  <li>Harmonising the <strong>support of government pre-school students, </strong>including equalizing per capita grants.</li>
  <li><strong>Improving the quality of pre-school teachers and teacher assistants, </strong>including 30,000 new and existing staff over the next three years.</li>
  <li><strong>Increasing the number of pre-school classes, </strong>in particular for the 4+ age group, estimated to be about 10,000 additional classes.</li>
  <li><strong>Increasing public-private partnerships in pre-school education </strong>to achieve additional classes required while adhering to national standards.</li>
  <li><strong>Developing a national pre-school information system</strong> to collate administrative information, such as physical location of the school, student and personnel particulars, and performance related data.</li>
</ul>
<h3>Ensuring that all children have acquired basic literacy and numeracy skills after three years of education</h3>
 <p> A new Literacy and Numeracy Programme (LINUS) has been designed to ensure that by 2012, all children will have basic literacy (in Bahasa Malaysia) and numeracy skills after three years of primary school education. Six ways have been identified to assist in achieving this:</p>
<ul>
  <li><strong>Screening conducted </strong>three times a year in March, June and September to help <strong>identify students </strong>who will either be <strong>placed into the LINUS programme or into a Special Education programme </strong>if they have a learning disability.</li>
  <li><strong>New literacy and numeracy modules developed</strong> where easier-to-grasp concepts will be covered earlier and more <strong>emphasis will be placed on the application of the concepts</strong> learned.</li>
  <li><strong>Training of approximately 17,000 teachers to deliver the new LINUS modules </strong>effectively, completed between November 2009 and February 2010.</li>
  <li><strong>Commitment built amongst stakeholders through awareness programmes and activities </strong>organized for state and district education departments, head teachers, parents, Teachers Associations and members of the public.</li>
  <li><strong>Intensive monitoring and supervision on the bottom 10% schools</strong> by officers in the state and district level education departments, and Ministry of Education’s Jemaah Nazir and Jaminan Kuality divisions.</li>
  <li><strong>Expert facilitators</strong> hired at the district level, attached to the District Education Department, <strong>to support LINUS teachers</strong> in schools.</li>
</ul>
<h3>Developing High Performance Schools (HPS) and lift the performance of all schools in the system</h3>
<p>High Performing Schools (HPS) are defined as strong schools with ethos, character and a unique identity enabling them to excel in all aspects of education. To qualify as an HPS, stringent criteria must be met including academic achievement, alumni strength, international recognition and a strong network of external relationships. </p>
<p>We are targeting to develop 20 HPS by 2010 and 100 HPS by 2012. HPS will be rewarded with the following benefits for them to strive to reach higher performance levels:</p>
<ul>
  <li><strong>Increased freedom in decision-making</strong>, such as flexibility in adapting curriculum and instruction methods as well as selection and redeployment of teachers (for unsatisfactory performance) along with allocation of funds.</li>
  <li><strong>Financial incentives</strong> for schools, school leaders, teachers and non-academic staff.</li>
  <li><strong>Greater range of options for human capital development</strong>, including sabbatical for head teachers.</li>
  <li>Option for <strong>high achievers (head teachers and principals) to advance faster</strong> through the education system for government schools. </li>
  <li><strong>Performance of schools will be based on a composite score</strong> consisting the Grade Point Average (70%) and Standard for Quality Education in Malaysia (SQEM) (30%).</li>
  <li><strong>Performance of all 9,900 government schools will be ranked on a yearly basis</strong> and made public from 2010 onwards.</li>
  <li>To be eligible <strong>for rewards, the school should achieve a specified target composite score or a target improvement in their performance rankings</strong>, obtain an unqualified financial audit report and achieve literacy and numeracy targets (for primary schools). In addition, the head teacher or principal must obtain an Annual Appraisal Report (LNPT) score greater than 90 and be free from disciplinary action.</li>
  <li><strong>Head teachers and principals</strong> who exceed the target <strong>will be given both financial and non-financial rewards</strong> (e.g. attachment programmes, accelerated promotion and certificates of recognition).</li>
  <li><strong>Teachers</strong> in schools with a head teacher or principal who qualifies for the reward <strong>will also be eligible for a financial reward.</strong></li>
</ul>
<p>However, having a HPS status comes the responsibility of sharing knowledge and expertise with other schools in the system via:</p>
<ul>
  <li>mentoring networks</li>
  <li>teacher attachment</li>
  <li>student exchange programmes</li>
</ul>
<p>Head teachers and principals will have to significantly improve their performance via performance management. Those in the 10% lowest percentile of performers (identified from their composite score) will be given training and mentoring in efforts for their improvement. However, if these methods prove ineffective, actions such as transfers and voluntary separation will be taken.</p>
